enum ElfNeed { lose, draw, win }

void main() {
  final rounds = puzzleInput.split('\n').map((e) => e.split(' ')).toList();

  var totalScore = 0;
  for (final round in rounds) {
    final opponent = round[0];
    final elf = round[1];

    var win = false;
    var draw = false;

    if ((opponent == 'A' && elf == 'X') ||
        (opponent == 'B' && elf == 'Y') ||
        (opponent == 'C' && elf == 'Z')) {
      draw = true;
    }

    if ((opponent == 'A' && elf == 'Y') ||
        (opponent == 'B' && elf == 'Z') ||
        opponent == 'C' && elf == 'X') {
      win = true;
    }

    if (win) {
      if (elf == 'X') totalScore += 1 + 6;
      if (elf == 'Y') totalScore += 2 + 6;
      if (elf == 'Z') totalScore += 3 + 6;
    } else if (draw) {
      if (elf == 'X') totalScore += 1 + 3;
      if (elf == 'Y') totalScore += 2 + 3;
      if (elf == 'Z') totalScore += 3 + 3;
    } else {
      if (elf == 'X') totalScore += 1;
      if (elf == 'Y') totalScore += 2;
      if (elf == 'Z') totalScore += 3;
    }
  }

  print('Part One: $totalScore');

  var newStratTotalScore = 0;
  for (final round in rounds) {
    final opponent = round[0];
    final elf = round[1];

    ElfNeed elfNeed;

    if (elf == 'X') {
      elfNeed = ElfNeed.lose;
    } else if (elf == 'Y') {
      elfNeed = ElfNeed.draw;
    } else {
      elfNeed = ElfNeed.win;
    }

    if (opponent == 'A') {
      switch (elfNeed) {
        case ElfNeed.lose:
          newStratTotalScore += 3; // Z
          break;
        case ElfNeed.draw:
          newStratTotalScore += 1 + 3; // X
          break;
        case ElfNeed.win:
          newStratTotalScore += 2 + 6; // Y
      }
    } else if (opponent == 'B') {
      switch (elfNeed) {
        case ElfNeed.lose:
          newStratTotalScore += 1; // X
          break;
        case ElfNeed.draw:
          newStratTotalScore += 2 + 3; // Y
          break;
        case ElfNeed.win:
          newStratTotalScore += 3 + 6; // Z
      }
    } else {
      switch (elfNeed) {
        case ElfNeed.lose:
          newStratTotalScore += 2; // Y
          break;
        case ElfNeed.draw:
          newStratTotalScore += 3 + 3; // Z
          break;
        case ElfNeed.win:
          newStratTotalScore += 1 + 6; // X
      }
    }
  }

  print('Part Two: $newStratTotalScore');
}

